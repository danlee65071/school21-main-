int		ft_intsize(int nbr)
{
	int	i;

	i = 0;
	if (!nbr)
		return (-1);
	else
	{
		while (nbr > 9)
		{
			nbr /= 10;
			i++;
		}
		i++;
		return (i);
	}
}

#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	k;
	char	*pos;

	k = 0;
	pos = (char *)b;
	while (k < len)
	{
		pos[k] = c;
		k++;
	}
	return (b);
}

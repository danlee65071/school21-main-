#include "push_swap.h"

void	ft_sa(int **a, int len_a, int is_ss)
{
	int	tmp;

	tmp = (*a)[len_a - 1];
	(*a)[len_a - 1] = (*a)[len_a - 2];
	(*a)[len_a - 2] = tmp;
	if (!is_ss)
		write(1, "sa\n", 3);
}

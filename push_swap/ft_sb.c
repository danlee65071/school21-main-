#include "push_swap.h"

void	ft_sb(int **b, int len_b, int is_ss)
{
	int	tmp;

	tmp = (*b)[len_b - 1];
	(*b)[len_b - 1] = (*b)[len_b - 2];
	(*b)[len_b - 2] = tmp;
	if (!is_ss)
		write(1, "sb\n", 3);
}
